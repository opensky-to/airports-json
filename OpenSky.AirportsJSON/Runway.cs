namespace OpenSky.AirportsJSON
{
    using System.Collections.Generic;

    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    /// A runway.
    /// </summary>
    /// <remarks>
    /// sushi.at, 03/12/2021.
    /// </remarks>
    /// -------------------------------------------------------------------------------------------------
    public class Runway
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets a value indicating whether this object has lighting.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public bool HasLighting { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the length of the runway in feet.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public int Length { get; set; }

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        /// Gets or sets the runway ends.
        /// </summary>
        /// -------------------------------------------------------------------------------------------------
        public List<RunwayEnd> RunwayEnds { get; set; } = new List<RunwayEnd>();
    }
}